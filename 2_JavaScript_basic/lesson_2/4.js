
'use strict';

/**
 * Сумма двух чисел
 * @param {number} a первое слагаемое
 * @param {number} b второе слагаемое
 * @returns {number} сумма a и b
 */
function getSumm (a = 0, b = 0) {
    return a + b;
}

/**
 * Разность двух чисел
 */
function getDiff (a = 0, b = 0) {
    return a - b;
}

/**
 * Деление двух чисел
 */
function getDivision (a = 0, b = 0) {
    return a / b;
}

/**
 * Произведение двух чисел
 */
function getMul (a = 0, b = 0) {
    return a * b;
}

