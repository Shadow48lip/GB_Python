# Выяснить тип результата выражений:
# 15 * 3
expression = 15 * 3
print(expression, type(expression))

# 15 / 3
expression = 15 / 3
print(expression, type(expression))

# 15 // 2
expression = 15 // 2
print(expression, type(expression))

# 15 ** 2
expression = 15 ** 2
print(expression, type(expression))